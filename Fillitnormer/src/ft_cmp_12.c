#include "fillit.h"

int		ft_cmp_12(int *tab)
{
	int v1[4];
	int i;

	v1[0] = 1;
	v1[1] = 2;
	v1[2] = 5;
	v1[3] = 9;
	i = 0;
	while (i != 4 && tab[i] == v1[i])
		i++;
	if (i == 4)
		return (1);
	return (0);
}
